// Curated dataset of all-time MLB players for the 162-0 draft game.
//
// Each player is tagged with the position they fill, the franchise they are
// most associated with for that era, the decade of their peak, and an overall
// rating (OVR, 1-100) used by the season simulation. Ratings are subjective,
// loosely calibrated to peak greatness — this is a game, not a stats engine.
//
// Coverage invariant: every (decade x position) cell has at least one player,
// so the slot machine can always land on a pickable team+decade for any round.

export type Position = 'SP' | 'C' | '1B' | '2B' | '3B' | 'SS' | 'LF' | 'CF' | 'RF';

export type Decade =
  | '1930s'
  | '1940s'
  | '1950s'
  | '1960s'
  | '1970s'
  | '1980s'
  | '1990s'
  | '2000s'
  | '2010s';

export interface Player {
  id: string;
  name: string;
  pos: Position;
  team: string; // franchise abbreviation, see TEAM_NAMES
  decade: Decade;
  ovr: number;
}

